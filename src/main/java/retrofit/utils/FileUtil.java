    /**
     * 复制assets里面的文件到指定的文件夹中
     *
     * @param context
     * @param path
     * @param assetsFileName
     * @throws IOException
     */
    public static void copyFileAssets(Context context, String path, String assetsFileName) throws IOException {
        InputStream in = null;
        OutputStream out = null;
        if (path != null) {
            File modelFile = new File(path);
            try {
                if (modelFile.exists())
                    modelFile.delete();
                modelFile.createNewFile();
                in = context.getApplicationContext().getAssets().open(assetsFileName);
                out = new FileOutputStream(modelFile);
                byte[] buffer = new byte[4096];
                int n;
                while ((n = in.read(buffer)) > 0) {
                    out.write(buffer, 0, n);
                }
            } catch (IOException e) {
                e.printStackTrace();
                modelFile.delete();
                throw e;
            } finally {
                if (in != null) {
                    in.close();
                }
                if (out != null) {
                    out.close();
                }
            }
        }
    }

    /**
     * 复制assets文件夹加下面的文件到SD卡
     *
     * @param context
     * @param assetDir
     * @param dir
     */
    public static void copyAssets(Context context, String assetDir, String dir) {
        String[] files;
        try {
            files = context.getResources().getAssets().list(assetDir);
        } catch (IOException e1) {
            return;
        }
        File mWorkingPath = new File(dir);
        // 如果目录不存在就创建目录
        if (!mWorkingPath.exists()) {
            if (!mWorkingPath.mkdirs()) {
            }
        }
        for (int i = 0; i < files.length; i++) {
            try {
                String fileName = files[i];
                // we make sure file name not contains '.' to be a folder.
                if (!fileName.contains(".")) {
                    if (0 == assetDir.length()) {
                        copyAssets(context, fileName, dir + fileName + "/");
                    } else {
                        copyAssets(context, assetDir + "/" + fileName, dir + fileName + "/");
                    }
                    continue;
                }
                File outFile = new File(mWorkingPath, fileName);
                if (outFile.exists())
                    outFile.delete();
                InputStream in = null;
                if (0 != assetDir.length())
                    in = context.getAssets().open(assetDir + "/" + fileName);
                else
                    in = context.getAssets().open(fileName);
                OutputStream out = new FileOutputStream(outFile);
                // Transfer bytes from in to out
                byte[] buf = new byte[1024];
                int len;
                while ((len = in.read(buf)) > 0) {
                    out.write(buf, 0, len);
                }
                in.close();
                out.close();
            } catch (FileNotFoundException e) {
                e.printStackTrace();
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }


