    /**
     * 将bitmap保存到指定的目录
     *
     * @param bitmap
     * @param savePath
     */
    public static String saveImage(Bitmap bitmap, String savePath, String imageName) {
        String path = "";
        FileOutputStream out = null;
        File dirFile = new File(savePath);  //目录转化成文件夹
        if (!dirFile.exists()) {           //如果不存在，那就建立这个文件夹
            dirFile.mkdirs();
        }
        File file = new File(savePath, imageName);
        try {
            out = new FileOutputStream(file);
            bitmap.compress(Bitmap.CompressFormat.JPEG, 80, out);
            path = file.getPath();
        } catch (FileNotFoundException e) {
            e.printStackTrace();
        }
        try {
            if (out != null) {
                out.flush();
                out.close();
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
        return path;
    }


    /**
     * 将输入流写入文件
     *
     * @param inputString
     * @param filePath
     */
    public static void writeFileLic(InputStream inputString, String filePath) throws Exception {
        File file = new File(filePath);
        if (file.exists()) {
            file.delete();
        }
        FileOutputStream fos = null;
        fos = new FileOutputStream(file);
        byte[] b = new byte[1024];
        int len;
        while ((len = inputString.read(b)) != -1) {
            fos.write(b, 0, len);
        }
        inputString.close();
        fos.close();
    }


